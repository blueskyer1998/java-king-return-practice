package com.blueskyer1998.ch03;

/**
 * 請列出 Unicode 碼值從 65 至 70 間的字元
 */
public class Ex03_04 {
    public static void main(String[] args) {
        char ch;

        ch = 65;
        System.out.println("變數 ch = 65 的內容 = " + ch);
        ch = 66;
        System.out.println("變數 ch = 66 的內容 = " + ch);
        ch = 67;
        System.out.println("變數 ch = 67 的內容 = " + ch);
        ch = 68;
        System.out.println("變數 ch = 68 的內容 = " + ch);
        ch = 69;
        System.out.println("變數 ch = 69 的內容 = " + ch);
        ch = 70;
        System.out.println("變數 ch = 70 的內容 = " + ch);
    }
}
