package com.blueskyer1998.ch03;

/**
 * 請將下列數值轉成 10 進位
 * (a) 0b11110010
 * (b) 07654
 * (c) 0xaaabbb
 */
public class Ex03_06 {
    public static void main(String[] args) {
        int x;

        x = 0b11110010;
        System.out.println(x);
        x = 07654;
        System.out.println(x);
        x = 0xaaabbb;
        System.out.println(x);
    }
}
