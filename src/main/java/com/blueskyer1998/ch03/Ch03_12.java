package com.blueskyer1998.ch03;

/**
 * 浮點數輸出的應用
 */
public class Ch03_12 {
    public static void main(String[] args) {
        double x;

        x = 1.05;
        System.out.println("變數x的值 = " + x);
        x = .789;
        System.out.println("變數x的值 = " + x);
        x = 5;
        System.out.println("變數x的值 = " + x);
        x = 1.29E-2;
        System.out.println("變數x的值 = " + x);
        x = 1.78E3;
        System.out.println("變數x的值 = " + x);
    }
}
