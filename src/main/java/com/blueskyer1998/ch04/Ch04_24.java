package com.blueskyer1998.ch04;

/**
 * 位元邏輯運算子的應用
 */
public class Ch04_24 {
    public static void main(String[] args) {
        byte x, y;
        x = 5;
        y = 1;
        System.out.println("x & y = " + (x & y));
        System.out.println("x | y = " + (x | y));
        System.out.println("x ^ y = " + (x ^ y));
    }
}
