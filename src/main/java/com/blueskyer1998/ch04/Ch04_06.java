package com.blueskyer1998.ch04;

/**
 * 遞增和遞減運算的基本應用。
 */
public class Ch04_06 {
    public static void main(String[] args) {
        int i = 10;
        System.out.println("i = " + i);
        i++; // 相當於 i = i + 1
        System.out.println("i = " + i);
        i--; // 相當於 i = i - 1
        System.out.println("i = " + i);
    }
}
